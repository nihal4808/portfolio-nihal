            const fileNames = fs.readdirSync(galleryDirectory);
            sampleImages = fileNames
                .filter((file) => /\.(jpg|jpeg|png|gif|webp)$/i.test(file))
                .map((file) => ({
                    src: `/gallery/${file}`,
                    alt: file,
                }));
